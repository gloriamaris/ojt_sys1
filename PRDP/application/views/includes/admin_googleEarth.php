					<h5> &nbsp; PRDP Applied Geotagging Tool</h5>
					<article>
					<div class="large 3 small-6 columns" style="font-size:12px;text-align:justify;">
					
						Awarded by NEDA as "2012 Best Practice Award", recognized by the World Bank and the President of the Republic of the Philippines, 
						President Benigno Simeon Aquino III as an "Efficient Transparency Tool" and "Project Management Tool" Applied Geotagging Tool is MRDP's 
						pioneering tools towards promoting "transparencies and total public disclosure" of sub-projects using open platform mapping applications 
						and <em>geotagging</em> which is now being adapted by PRDP on promoting same purpose and using the tool to complement project management. Its basic 
						open technology is using a digital camera with build-in GPS (Global Positioning System), which is a common feature on smartphones and tablets. 
					
					<br/><br/><br/>
